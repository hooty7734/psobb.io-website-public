if ($account_id) {
    // Retrieve the point telemetry for the active player across all community campaigns
    $uc_res = $db->prepare("SELECT event_id, contribution_count FROM community_event_participants WHERE account_id = :accId");
    $uc_res->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $uc_exec = $uc_res->execute();
    if ($uc_exec) {
        while ($uc_row = $uc_exec->fetchArray(SQLITE3_ASSOC)) {
            $user_contributions[$uc_row['event_id']] = $uc_row['contribution_count'];
        }
    }
}

// Get active events
$ce_res = $db->query("SELECT id, title, description, goal_type, goal_target, target_amount, current_progress, reward_item_string, top_3_reward_item_string
                      FROM community_events 
                      WHERE status = 'active'
                      ORDER BY id DESC");
$top_contributors = [];
while ($row = $ce_res->fetchArray(SQLITE3_ASSOC)) {
