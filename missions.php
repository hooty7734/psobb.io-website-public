                            FROM community_events ce
                            JOIN community_event_participants cep ON ce.id = cep.event_id
                            WHERE ce.status = 'completed' AND cep.account_id = :accId AND cep.reward_claimed = 0");
    $cc_res->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $cc_exec = $cc_res->execute();
    while ($row = $cc_exec->fetchArray(SQLITE3_ASSOC)) {
        $top3_stmt = $db->prepare("SELECT account_id FROM community_event_participants WHERE event_id = :eid ORDER BY contribution_count DESC LIMIT 3");
