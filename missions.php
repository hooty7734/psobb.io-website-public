    $pr_res = $db->prepare("SELECT pm.id as pm_id, pm.character_name, m.title, m.description, m.goal_type, m.goal_target, m.reward_item_string 
                            FROM player_missions pm 
                            JOIN missions m ON pm.mission_id = m.id 
                            WHERE pm.account_id = :accId AND pm.status = 'ready_to_redeem' 
                            ORDER BY (m.goal_type = 'HARDCORE_MENTOR' OR m.goal_type = 'DIVERSE_PARTY_BOSS') DESC, pm.id DESC");
    $pr_res->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $pr_exec = $pr_res->execute();
    while ($row = $pr_exec->fetchArray(SQLITE3_ASSOC)) {
        $pending_redemptions[] = $row;
    }
}

// -------------------------------------------------------------------------
// 1. Fetch Global Top 10 Leaderboard
// Ranks players on the frontend based on the total aggregate of all completed bounties.
// -------------------------------------------------------------------------
$leaderboard = [];
$lb_res = $db->query("SELECT COALESCE(u.display_name, 'Hunter #' || u.account_id) as hunter_name, COUNT(pm.id) as completions 
                      FROM player_missions pm 
                      JOIN users u ON pm.account_id = u.account_id 
                      WHERE pm.status = 'completed' 
                      GROUP BY u.account_id 
                      ORDER BY completions DESC 
                      LIMIT 3");
while ($row = $lb_res->fetchArray(SQLITE3_ASSOC)) {
    $leaderboard[] = $row;
}

// -------------------------------------------------------------------------
// 2. Fetch Global Active Bounties Feed
// Populates the live feed of in-progress bounties dynamically generated by Gemini.
// -------------------------------------------------------------------------
$active_bounties = [];
$ab_query = "SELECT pm.id as pm_id, pm.account_id as bounty_account_id, COALESCE(u.display_name, 'Hunter #' || u.account_id) as hunter_name, pm.character_name, m.title, m.description, m.goal_type, m.goal_target, m.reward_item_string 
             FROM player_missions pm 
             JOIN missions m ON pm.mission_id = m.id 
             JOIN users u ON pm.account_id = u.account_id 
             WHERE pm.status = 'in_progress' ";

if ($account_id) {
    // SQLite boolean expression: (u.account_id = X) evaluates to 1 or 0. Sorting DESC puts 1s at the top!
    $ab_query .= "ORDER BY (u.account_id = :accId) DESC, pm.id DESC";
    $ab_stmt = $db->prepare($ab_query);
    $ab_stmt->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $ab_res = $ab_stmt->execute();
} else {
    $ab_query .= "ORDER BY pm.id DESC";
    $ab_res = $db->query($ab_query);
}
while ($row = $ab_res->fetchArray(SQLITE3_ASSOC)) {
    $active_bounties[] = $row;
}

// -------------------------------------------------------------------------
// 3. Fetch Community Events (Active and Claimable)
// -------------------------------------------------------------------------
$active_community_events = [];
$claimable_community_events = [];

// Get active events
$ce_res = $db->query("SELECT id, title, description, goal_type, goal_target, target_amount, current_progress, reward_item_string, top_3_reward_item_string
                      FROM community_events 
                      WHERE status = 'active'
                      ORDER BY id DESC");
$top_contributors = [];
while ($row = $ce_res->fetchArray(SQLITE3_ASSOC)) {
    $active_community_events[] = $row;
    
    // Fetch Top 3
    $top_stmt = $db->prepare("SELECT COALESCE(u.display_name, 'Hunter #' || u.account_id) as hunter_name, cep.contribution_count FROM community_event_participants cep JOIN users u ON cep.account_id = u.account_id WHERE cep.event_id = :eid ORDER BY cep.contribution_count DESC LIMIT 3");
    $top_stmt->bindValue(':eid', $row['id'], SQLITE3_INTEGER);
    $top_exec = $top_stmt->execute();
    while ($t_row = $top_exec->fetchArray(SQLITE3_ASSOC)) {
        $top_contributors[$row['id']][] = $t_row;
    }
}

// Get claimable events for the current user
if ($account_id) {
    $cc_res = $db->prepare("SELECT ce.id as event_id, ce.title, ce.description, ce.reward_item_string, ce.top_3_reward_item_string, cep.contribution_count
                            FROM community_events ce
                            JOIN community_event_participants cep ON ce.id = cep.event_id
                            WHERE ce.status = 'completed' AND cep.account_id = :accId AND cep.reward_claimed = 0");
    $cc_res->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $cc_exec = $cc_res->execute();
    while ($row = $cc_exec->fetchArray(SQLITE3_ASSOC)) {
        $top3_stmt = $db->prepare("SELECT account_id FROM community_event_participants WHERE event_id = :eid ORDER BY contribution_count DESC LIMIT 3");
        $top3_stmt->bindValue(':eid', $row['event_id'], SQLITE3_INTEGER);
        $top3_res = $top3_stmt->execute();
        $is_top_3 = false;
        while ($t3 = $top3_res->fetchArray(SQLITE3_ASSOC)) {
            if ($t3['account_id'] == $account_id) {
                $is_top_3 = true;
                break;
            }
        }
        $row['is_top_3'] = $is_top_3;
        $claimable_community_events[] = $row;
    }
}

