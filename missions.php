             FROM player_missions pm 
             JOIN missions m ON pm.mission_id = m.id 
             JOIN users u ON pm.account_id = u.account_id 
             WHERE pm.status = 'in_progress' ";

if ($account_id) {
    // SQLite boolean expression: (u.account_id = X) evaluates to 1 or 0. Sorting DESC puts 1s at the top!
    $ab_query .= "ORDER BY (u.account_id = :accId) DESC, pm.id DESC";
    $ab_stmt = $db->prepare($ab_query);
    $ab_stmt->bindValue(':accId', $account_id, SQLITE3_INTEGER);
    $ab_res = $ab_stmt->execute();
} else {
    $ab_query .= "ORDER BY pm.id DESC";
    $ab_res = $db->query($ab_query);
}
while ($row = $ab_res->fetchArray(SQLITE3_ASSOC)) {
    $active_bounties[] = $row;
}

// -------------------------------------------------------------------------
// 3. Fetch Community Events (Active and Claimable) & User Contributions
// -------------------------------------------------------------------------
